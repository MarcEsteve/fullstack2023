// función de flecha para la SUMA
var sumar3 = function (param1, param2) {
    return param1 + param2;
};
console.log(sumar3(6, 3));
// forma simplificada de la función de flecha
var sumarSimple3 = function (param1, param2) { return param1 + param2; };
console.log(sumarSimple3(6, 3));
